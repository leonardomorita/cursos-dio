preco_produto_1 = 20
preco_produto_2 = 10

print(preco_produto_1 + preco_produto_2)
print(preco_produto_1 - preco_produto_2)
print(preco_produto_1 * preco_produto_2)
print(preco_produto_1 / preco_produto_2)
print(preco_produto_1 // preco_produto_2) # Divisão com resultado inteiro
print(preco_produto_1 ** preco_produto_2) # Exponenciação
print(preco_produto_1 % preco_produto_2) # Resto da divisão

